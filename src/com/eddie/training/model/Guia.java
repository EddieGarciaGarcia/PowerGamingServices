package com.eddie.training.model;

public class Guia {
	private Integer idGuia;
	private String nombre;
	
	public Guia() {}
	
	public Integer getIdGuia() {
		return idGuia;
	}
	public String getNombre() {
		return nombre;
	}
	public void setIdGuia(Integer idGuia) {
		this.idGuia = idGuia;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
